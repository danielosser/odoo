/** @odoo-module **/

import { Define } from '@mail/define';

export default Define`
    {Dev/comment}
        Show (editable) list of subtypes of this follower.
    {Action}
        [Action/name]
            Follower/showSubtypes
        [Action/params]
            follower
                [type]
                    Follower
        [Action/behavior]
            :subtypesData
                {Record/doAsync}
                    [0]
                        @follower
                    [1]
                        {func}
                            @env
                            .{Env/owlEnv}
                            .{Dict/get}
                                services
                            .{Dict/get}
                                rpc
                            .{Function/call}
                                [route]
                                    /mail/read_subscription_data
                                [params]
                                    [follower_id]
                                        @follower
                                        .{Follower/id}
            {Record/update}
                [0]
                    @follower
                [1]
                    [Follower/subtypes]
                        {Record/empty}
            {foreach}
                @subtypesData
            .{as}
                data
            .{do}
                :subtype
                    {Record/insert}
                        [Record/traits]
                            FollowerSubtype
                        {FollowerSubtype/convertData}
                            @data
                {Record/update}
                    [0]
                        @follower
                    [1]
                        [Follower/subtypes]
                            {Field/add}
                                @subtype
                {if}
                    @data
                    .{Object/get}
                        followed
                .{then}
                    {Record/update}
                        [0]
                            @follower
                        [1]
                            [Follower/selectedSubtypes]
                                {Field/add}
                                    @subtype
                .{else}
                    {Record/update}
                        [0]
                            @follower
                        [1]
                            [Follower/selectedSubtypes]
                                {Field/remove}
                                    @subtype
            {Record/update}
                [0]
                    {Env/dialogManager}
                [1]
                    [DialogManager/dialogs]
                        {Field/add}
                            {Record/insert}
                                [Record/traits]
                                    Dialog
                                [Dialog/followerSubtypeList]
                                    {Record/insert}
                                        [Record/traits]
                                            FollowerSubtypeList
                                        [FollowerSubtypeList/follower]
                                            @record
`;
