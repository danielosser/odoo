/** @odoo-module **/

import { Define } from '@mail/define';

export default Define`
    {Dev/comment}
        Search for partners matching 'keyword'.
    {Action}
        [Action/name]
            Partner/imSearch
        [Action/params]
            callback
                [type]
                    Function
            keyword
                [type]
                    String
            limit
                [type]
                    Integer
                [default]
                    10
            record
                [type]
                    Partner
        [Action/behavior]
            {Dev/comment}
                prefetched partners
            :partners
                {Record/insert}
                    [Record/traits]
                        Collection
            :cleanedSearchTerm
                {Utils/cleanSearchTerm}
                    @keyword
            {foreach}
                {Record/all}
                    [Record/traits]
                        Partner
                    {func}
                        [in]
                            item
                        [out]
                            @item
                            .{Partner/active}
            .{as}
                partner
            .{do}
                {if}
                    @partners
                    .{Collection/length}
                    .{<}
                        @limit
                .{then}
                    {if}
                        @partner
                        .{!=}
                            {Env/currentPartner}
                        .{&}
                            @partner
                            .{Partner/name}
                        .{&}
                            @partner
                            .{Partner/user}
                        .{&}
                            {Utils/cleanSearchTerm}
                                @partner
                                .{Partner/name}
                            .{String/includes}
                                @cleanedSearchTerm
                    .{then}
                        {Collection/push}
                            [0]
                                @partners
                            [1]
                                @partner
            {if}
                @partners
                .{Collection/length}
                .{=}
                    0
            .{then}
                :partnersData
                    @env
                    .{Env/owlEnv}
                    .{Dict/get}
                        services
                    .{Dict/get}
                        rpc
                    .{Function/call}
                        [0]
                            [model]
                                res.partner
                            [method]
                                im_search
                            [args]
                                {Record/insert}
                                    [Record/traits]
                                        Collection
                                    [0]
                                        @keyword
                                    [1]
                                        @limit
                        [1]
                            [shadow]
                                true
                :newPartners
                    {Record/insert}
                        [Record/traits]
                            Partner
                        @partnersData
                        .{Collection/map}
                            {func}
                                [in]
                                    item
                                [out]
                                    {Partner/convertData}
                                        @item
                {Collection/push}
                    [0]
                        @partners
                    [1]
                        @newPartners
            @callback
            .{Function/call}
                @partners
`;
