/** @odoo-module **/

import { Define } from '@mail/define';

export default Define`
    {Dev/comment}
        Refresh followers information from server.
    {Action}
        [Action/name]
            Thread/refreshFollowers
        [Action/params]
            thread
                [type]
                    Thread
        [Action/behavior]
            {if}
                @thread
                .{Thread/isTemporary}
            .{then}
                {Record/update}
                    [0]
                        @thread
                    [1]
                        [Thread/followers]
                            {Record/empty}
                {break}
            :followers
                {Record/doAsync}
                    [0]
                        @thread
                    [1]
                        {func}
                            @env
                            .{Env/owlEnv}
                            .{Dict/get}
                                services
                            .{Dict/get}
                                rpc
                            .{Function/call}
                                [0]
                                    [route]
                                        /mail/read_followers
                                    [params]
                                        [res_id]
                                            @thread
                                            .{Thread/id}
                                        [res_model]
                                            @thread
                                            .{Thread/model}
                                [1]
                                    [shadow]
                                        true
                .{Dict/get}
                    followers
            {Record/update}
                [0]
                    @thread
                [1]
                    [Thread/areFollowersLoaded]
                        true
            {if}
                @followers
                .{Collection/length}
                .{>}
                    0
            .{then}
                {Record/update}
                    [0]
                        @thread
                    [1]
                        [Thread/followers]
                            @followers
                            .{Collection/map}
                                {func}
                                    [in]
                                        item
                                    [out]
                                        {Record/insert}
                                            [Record/traits]
                                                Follower
                                            {Follower/convertData}
                                                @item
            .{else}
                {Record/update}
                    [0]
                        @thread
                    [1]
                        [Thread/followers]
                            {Record/empty}
`;
